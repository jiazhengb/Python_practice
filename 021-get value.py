def gather_values(x):
    '''
    Write a function gather_values that can produce the following output from x:
    {'10': [1, 1, 1, 1, 1],
    '11': [1, 1, 1, 1, 1, 1],        
    '01': [1, 1, 1],                 
    '00': [0, 0, 0, 0, 0, 0]}

    :param x: input list
    :type x: list
    '''
    assert isinstance(x, list)
    for l in x:
        assert isinstance(l, str)
        for i in range(len(l)):
            assert l[i] == '0' or '1'
    assert len(x) > 0
    h = 0
    c = 0
    d = {}
    y = list(dict.fromkeys(x))
    a = []
    ans = []
    for l in x:
        for i in range(len(l)):
            if l[i] == '0':
                h += 1
            else:
                h -= 1
        if h > 0:
            ans.append(0)
        else:
            ans.append(1)
        h = 0

    for i in range(len(y)):
        for z in x:
            if z == y[c]:
                a.append(ans[x.index(z)])
        d[y[c]] = a
        a = []
        c += 1
    return d

